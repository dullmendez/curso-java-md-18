public class ClaseSwitch{
    public static void main(String args[]){
        int dia = 3;
        switch (dia){
            case 1:
                System.out.println("Es lunes. odiado por garfield");
            break;
            case 2:
            System.out.println("Martes de frescura");
            break;
            case 3:
            System.out.println("Miércoles de Plaza");
            break;
            case 4:
            System.out.println("Jueves Pozolero");
            break;
            case 5:
            System.out.println("Viernes y el cuerpo lo sabe");
            break;
            default:
            System.out.println("Es sábadrink y dominguito de dormir");
            break;
        }
    }
}